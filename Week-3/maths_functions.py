import math

#using the math library

x = math.pow(2,5)
print(x)
print("{} to power {} = {}".format(str(2),str(5),str(x)))

#square root of 16
z = math.sqrt(16)
print(z)

#trigonometric values
cos_x = math.cos(60)
sin_x = math.sin(60)
tan_x = math.tan(60)
print(cos_x)
print(sin_x)
print(tan_x)

